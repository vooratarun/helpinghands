<div id="h2"><b>Discipline Measurements</b></div><br>
<div id="p2" style="text-align:justify;">
Helping Hands would like to adopt its own discipline measurements. The volunteers working in Helping Hands Organization should be self- disciplined and must act as per the rules and regulations. If anyone in the organization is seen violating the rules and regulations of Helping Hands, He/she would be suspended immediately from Helping Hands Organization.
<br><br><div id="psub" style="text-align:justify;">
The following are some of the indisciplinary things which come under the discipline measurements and have been classified into different clauses. 
<br><br><div id="p">
<ul>Clause-I: Money oriented issues</ul>
</div>

<div id="p2">

<li>Stealing money from the Helping Hands</li><br>
<li>Encouraging ineligible students for their personal benefits </li><br>
<li>Providing incorrect information and proofs for getting the money from helping hands (Ex: medical bills, travelling charges...etc)</li><br>
<li>Unauthorized collection of money</li><br>

</div>


<div id="p">
<ul>Clause-II:  Behavior oriented issues</ul>
</div>

<div id="p2">
<li>Violation of rules framed for smooth running of helping hands. </li><br>
<li>Unnecessary interactions in the name of Helping Hands </li><br>
<li>Unnecessary arguments and comments with subordinates</li><br>
<li>Spreading rumors about Helping Hands</li><br>
<li>Being inactive at the work place</li><br>
</div>

<div id="p">
<ul>Clause-III: Regularity and other issues</ul>
</div>

<div id="p2">
<li>Not attending to the meetings or activities conducted by helping hands without prior notice and proper reason </li><br>
<li>Failing to bring to the notice of EC about the needy people without any reason even after their request to him</li><br>
</div>

<div id="p">
<ul>Clause-IV: Penalties for the members falling under above issues</ul>
</div>

<div id="p2">
<li>The person falling under any Clause should be suspended immediately.</li><br>
<li>Subjected to the severity of the situation, he/she should be sent to the discipline committee of our campus.</li><br>
<li>In case of money thefting or misusing, along with above two actions, money also should be recovered from the person</li><br>
</div>

<br><b style="margin-left:15px;margin-right:50px;font-size:20">Process Description</b><br><br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;Any student who wants to approach ICD for financial help has to fill in the application form (Pl find the attachment in Annex – I) and submit it to the class HH representative. The application forms will be available with the cluster/floor/branch level HH representatives. All applications will be scrutinized at four levels.
</p>
<li>Duly filled application has to be submit along with the required documents and two neighbour class HH representative sign to the class HH representative. If the class HH representative, upon scrutiny, is satisfied with claims of the applicant he/she will forward the application to the respective cluster/floor/branch HH representative. </li>
<br>
<li>If the cluster/floor, branch representative, upon scrutiny, is satisfied with the claims of the applicant he/she will forward the application to the respective class/branch EC member. </li>
<br>
<li>If the EC member finds that the case is genuine he/she will personally handover the application to the Joint Secretory of ICD.</li>
<br>
<li>The Joint secretory of ICD after verifying the application approves the amount of financial grant to be provided and forwards the application to the finance dept. The finance dept., based on the recommendations of the Joint secretory of ICD will disburse the grant to a student.</li>
<br>
<li>Sanction money if more than 1000 Rs. then the Joint secretory of ICD need to arrange EC meeting after consulting with general secretary for necessary approval.</li>
<br>
<li>If an application successfully reaches the Joint secretory of ICD it means that the application is accepted unless the Joint secretory strongly feels for a fresh scrutiny.  The class HH representative, cluster/floor, branch representatives and the EC members have to carefully verify the case before forwarding it to the next level.  </li>
<br>
<li>An EC member from ICD will personally handover the applications to the Joint secretory/representative of finance dept. Finance dept. will release fund after verification and send back the application through finance representative to ICD for documentation.  </li>

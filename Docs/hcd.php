	  <h2 style="font-style:underline;"><u>Health Care Department (HCD)</u></h2>
     <h4>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Objective</h4>
	<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
	To identify and serve the poor & needy students who are suffering with health related problems which are beyond the range of university hospital so that they can continue their educational journey in the campus with more confidence.
        </p>
     <h4>&nbsp;&nbsp;&nbsp;&nbsp;Functions of HCD:</h4>
<ul type="circle" style="margin-left:60px;">
<li> Conduct General and Expert Health camps periodically.</li>
<li>Medical allowance to the needy RGUKT Nuzvid students </li>
<li>Outside Charity in terms of Health </li>
</ul>
     <h4>&nbsp;Structure of Health Care Department (HCD)</h4>

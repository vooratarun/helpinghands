        </p>
      <h4> Budget Allotment </h4>
	<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The HCD receives 40 % of the total budget allotment in each financial year.  </p>

    </p>
      <h4> Process of Functioning: </h4>
	<p style="font-size:18px;margin-left:20px;">Any student who wants to approach HCD for Medical help has to fill in the application form (Pl find the attachment in Annex – I) and submit it to the class HH representative. The application forms will be available with the cluster/floor/branch level HH representatives. All applications will be scrutinized at four levels.</p><br>
<ul type="circle" style='margin-left:40px;'>
<li>Duly filled application has to be submit along with the required documents and two neighbour class HH representative sign to the class HH representative. If the class HH representative, upon scrutiny, is satisfied with claims of the applicant he/she will forward the application to the respective cluster/floor/branch HH representative. </li><li>
If the cluster/floor, branch representative, upon scrutiny, is satisfied with the claims of the applicant he/she will forward the application to the respective class/branch EC member. </li>

<li>If the EC member finds that the case is genuine he/she will personally handover the application to the Joint Secretory of HCD.</li>
<li>The Joint secretory of HCD after verifying the application approves the amount of financial grant to be provided and forwards the application to the finance</li> dept. The finance dept., based on the recommendations of the Joint secretory of HCD will disburse the grant to a student.</li>
<li>Sanction money if more than 5000 Rs. then the Joint secretory of HCD need to arrange EC meeting after consulting with General Secretary for necessary approval.</li>
<li>If an application successfully reaches the Joint secretory of HCD it means that the application is consider for final acceptance unless the Joint secretory strongly feels for a fresh scrutiny.  The class HH representative, cluster/floor, branch representatives and the EC members have to carefully verify the case before forwarding it to the next level. </li>
<li>
An EC member from HCD will personally handover the applications to the Joint secretory/representative of finance dept. Finance dept. will release fund after verification and send back the application through finance representative to HCD for documentation.<Br><Br>
<b><center>Note: Cluster rep. for PUC, Floor rep. for E-1 and Brach rep. for E-2, E-3 and E-4.</center></b><br>
</li>
<li>Conduct General & Expert Health camps periodically and Outside Charity in terms of Health case Joint Secretory of HCD need to arrange EC meeting after consulting with General Secretary for necessary approval.</li>
</ul>
  </p>

<h4>For Students</h4>
<p>
<ul type="circle" style='margin-left:40px;'>
<li>Any student who wants to approach Health Care Department (HCD) for help have to fill the application (Available with the cluster/floor/branch HH representative) and attach copies of the required documents.</li>
<li>The duly filled application has to be submit along with the required documents and two neighbour class HH representative sign to the class HH representative.</li> <li>The application will be processed through class HH representative and to cluster/floor/branch representative and to student EC member and finally to the Joint secretory of HCD. </li>
<li>If required applicant may need to present himself/herself before the Joint secretory of HCD.</li>
</ul>

</p>

<h4>Documents Required</h4>
<p>

Photo copies/Xerox of the following documents have to be attached along with the application<Br>
<ul type="circle" style='margin-left:40px;'>
<li> Income certificate/white ration card
<li>College ID proof 
<li> Proof of Prescription or report given by Doctors (Preferable M.S or MD)
<li>Proof of Bills need to submit after completion of treatment.
</ul>
</p>

<h4>For Class HH Representatives</h4>
<p>
<ul type="circle" style='margin-left:40px;' >
<li>The Class HH Representative have to guide the students regarding where the applications are available (applications are with the cluster/floor/branch HH representatives). </li>
<li>Help the applicant in completing the form, if needed.</li>
<li>HH Representative is required to receive the filled application along with the needed attachments from the applicant and scrutinize it. </li>
<li>HH Representative has to verify whether all details in the application are completed and all the necessary proofs are attached along with sign. </li>
<li>If HH Representative finds that the case is genuine then he/she has to personally submit them to respective cluster/floor/branch HH representative. </li>
<li>HH Representative has to scrutinize the application within <b>one working day.</b></li>
</p>
</ul>
<h4>For Cluster/Floor/Branch HH Representatives</h4>

<ul type="circle" style='margin-left:40px;' >
<p>
<li>You have to maintain application forms with you and should make them available to the students.</li>
<li>The filled in applications forms will be forwarded to you from the class HH representatives with their signature. </li>
<li>You may need to meet the class rep. to verify the case and if you are satisfied you have to personally submit the application with your signature to the respective EC member. </li>
<li>If you are not satisfied with the case please return the application to the class HH rep. stating your doubts.</li>
<li>You have to process the application to the next level within one working day.</li>
</ul>
</p>
<h4>For HCD EC Members</h4>
<p>
<ul type="circle" style='margin-left:40px;' >
<li>EC Members will receive the filled application from the respective cluster/floor/branch HH rep.</li>
<li>EC Members are required to verify the facts mentioned in the application.</li>
<li>EC Members may meet class rep. or cluster/floor/branch rep. if needed.</li>
<li>If EC Members are satisfied he/she have to personally submit to the Joint secretory of HCD.</li>
<li>If EC Members are not satisfied he/she have to return the application to the class HH rep.</li>
<li>EC Members have to process the application in one working day. </li>
</ul>
</p>
<h4>For the Joint secretory of HCD</h4>
<p>
<ul type="circle" style='margin-left:40px;'>

<li>Joint secretory will receive the applications from the respective student EC members.</li>
<li>Joint secretory will endorse to finance dept to release the fund by keeping the record in proper category wise and are required to process the applications in one working day.</li>
<li>If Joint secretory strongly feel that further scrutiny is needed then he/she may take necessary steps in this regard.</li>
<li>Track the record of allocated budget.</li>
</ul>
</p>
<h4>Rules and regulations</h4>
<ul type="circle" style='margin-left:40px;'>
<li>HCD will consult other faculty EC members & Advisory committee , in case it has to extend its services to the people other than the student (parents, relatives, staff) as an exceptional case</li>
<li>HCD responsibility is strictly restricted to student’s health issues not their parents, relatives or any other staff / Faculty members in the campus.</li>
<li>HCD generally not responsible for the accidents happened outside the campus. However the situation will be viewed in humanitarian grounds & it may extend its support in all the possible & eligible cases</li>
<li>HCD never ensure the full reimbursement / full payment for treatment or medicine. Student has to bare some minimum amount from his personal front. However this minimum amount varies from case to case.</li>
<li>Preference is given only for the RGUKT Nuzvid campus students. In this regard maximum 1/10th of total medical allocation will be provide per student, during serious case maximum limit will be change according to severity of case and it will be decide by only EC meeting.</li>
<li>Necessary proofs have to produce for utilizing the medical allowance. (i.e. Medical report/prescription given by M.S/M.D/highly educated Doctors) otherwise application will be rejected.</li>
<li>Medical allowance sanctioned students should have to produce bills during or after the treatment and one zerox copy of certificate and bill need to submit to committee otherwise next time medical help will not be entitle to him/her.</li>
<li>Sanction money if more than 1000 Rs. then transaction should be in check form only. </li>
<li>Students, who are suffering with “long term diseases”, have to renewal every academic year and separate file will be maintain for them and grant will be sanction maximum of  4 years only.</li>

